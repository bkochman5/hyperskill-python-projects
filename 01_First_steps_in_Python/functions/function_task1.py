# Define a function that accepts the number of miles and returns this distance in kilometers.)
#
# Assume that one mile is approximately equal to 1.609 kilometers.
#
# You don't have to handle input, just implement the function.


def mi_to_km(miles):
    km = miles * 1.609
    return km
print(mi_to_km(miles=100))